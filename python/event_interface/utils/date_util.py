#! /usr/bin/env python
# -*- coding: utf-8 -*-

import time


def gener_date_8bit():
    even_time = time.strftime('%Y%m%d', time.localtime(time.time()))
    return even_time


def gener_time_6bit():
    even_time = time.strftime('%H%M%S', time.localtime(time.time()))
    return even_time


def gener_timestamp():
    even_time = time.strftime('%Y-%m-%d %H:%M:%S', time.localtime(time.time()))
    return even_time


if __name__ == '__main__':
    print(gener_date_8bit())
    print(gener_time_6bit())
    print(gener_timestamp())